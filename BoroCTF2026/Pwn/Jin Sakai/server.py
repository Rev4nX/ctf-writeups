def clear(): sys.stdout.write("\033[2J\033[H")

def typewriter(text, color="\033[36m"):
    sys.stdout.write(color)
    for char in text:
        sys.stdout.write(char)
        sys.stdout.flush()
        time.sleep(0.015)
    sys.stdout.write("\033[0m\n")

def transition():
    chars = "!@#$%^&*()_+-=[]{}|<>?"
    for _ in range(8):
        clear()
        glitch = "".join(random.choice(chars) if c not in " \n." else c for c in P1_ASCII)
        sys.stdout.write("\033[31m" + glitch + "\033[0m\n")
        sys.stdout.flush()
        time.sleep(0.05)
    
    clear()
    time.sleep(0.5)
    
    lines = P2_ASCII_NO_EYE.strip('\n').split('\n')
    for i in range(len(lines)):
        clear()
        sys.stdout.write("\n" * (len(lines) - i - 1))
        sys.stdout.write("\033[90m" + "\n".join(lines[:i+1]) + "\033[0m\n")
        sys.stdout.flush()
        time.sleep(0.02)
      
    time.sleep(0.5)
    

def render_state(phase, hp, boss_msg, prompt_msg):
    clear()
    if phase == 1:
        sys.stdout.write("\033[90m" + P1_ASCII + "\033[0m\n")
        hp_color = "\033[32m"
    else:
        sys.stdout.write("\033[90m" + P2_ASCII_EYE + "\033[0m\n")
        hp_color = "\033[31m"
        
    print("\033[37m" + "="*80 + "\033[0m")
    print(f" {hp_color}SAMURAI HP {hp:<20}\033[0m \033[33mPHASE {phase}/2\033[0m")
    print("\033[37m" + "="*80 + "\033[0m")
    
    if boss_msg:
        sys.stdout.write(" \033[37m[\033[31mBOSS\033[37m]\033[0m ")
        typewriter(boss_msg, "\033[36m" if phase==1 else "\033[31;1m")
        print("\033[37m" + "-"*80 + "\033[0m")
        
    if prompt_msg:
        print(f" \033[33m>>> {prompt_msg}\033[0m")
        sys.stdout.write(" \033[32m>\033[0m ")
        sys.stdout.flush()

def main():
    p = subprocess.Popen(['./boss'], stdin=subprocess.PIPE, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    phase = 1
    hp = "999"
    last_msg = ""
    
    while True:
        line_raw = p.stdout.readline()
        if not line_raw: break
        
        try: line = line_raw.decode('utf-8').strip()
        except: continue
            
        if line.startswith("PHASE|"):
            phase = int(line.split('|')[1])
        elif line.startswith("HP|"):
            hp = line.split('|')[1]
        elif line.startswith("MSG|"):
            last_msg = line.split('|')[1]
        elif line.startswith("PROMPT|"):
            prompt_msg = line.split('|')[1]
            render_state(phase, hp, last_msg, prompt_msg)
            payload = sys.stdin.buffer.readline()
            p.stdin.write(payload)
            p.stdin.flush()
        elif line.startswith("TRANSITION|"):
            transition()
        elif line.startswith("DIE|"):
            print("\n\033[41;37m  *** YOU DIED. YOUR VISION FADES TO BLACK. *** \033[0m\n")
            break
        elif line.startswith("WIN|"):
            print("\n\033[42;30m  *** THE BEAST SHATTERS INTO DUST. YOU ARE VICTORIOUS! *** \033[0m\n")
        elif line.startswith("FLAG|"):
            print(f"  \033[33;1m{line.split('|')[1]}\033[0m\n")
            break

if __name__ == "__main__":
    main()
